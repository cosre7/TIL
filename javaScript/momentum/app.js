const calculator = {
    add: function(a, b) {
        console.log(a + b);
    },
    minus: function(a, b) {
        console.log(a - b);
    },
    times: function(a, b) {
        console.log(a * b);
    },
    divide: function(a, b) {
        console.log(a / b);
    },
    power: function(a, b) {
        console.log(a**b);
    }
}

calculator.add(10, 30);
calculator.minus(10, 30);
calculator.times(10, 30);
calculator.divide(10, 30);
calculator.power(10, 30);